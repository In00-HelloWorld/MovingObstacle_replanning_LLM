def smoothstep(tau):
    tau = np.clip(tau, 0.0, 1.0)
    return 10 * tau**3 - 15 * tau**4 + 6 * tau**5

def interpolate_xyz(p0, p1, tau):
    s = smoothstep(tau)
    return (1.0 - s) * np.array(p0, dtype=float) + s * np.array(p1, dtype=float)

def desired_position_from_schedule(schedule, t_now):
    if t_now <= schedule[0][0]:
        return np.array(schedule[0][1], dtype=float)

    for i in range(len(schedule) - 1):
        t0, p0 = schedule[i]
        t1, p1 = schedule[i + 1]
        if t0 <= t_now <= t1:
            if t1 == t0:
                return np.array(p1, dtype=float)
            tau = (t_now - t0) / (t1 - t0)
            return interpolate_xyz(p0, p1, tau)

    return np.array(schedule[-1][1], dtype=float)

def desired_position_with_landing(drone_name, t_now):
    schedule = MISSION_SCHEDULES[drone_name]

    if t_now <= MISSION_END_TIME:
        return desired_position_from_schedule(schedule, t_now)

    t_land = t_now - MISSION_END_TIME
    p_start = np.array(schedule[-1][1], dtype=float)
    p_end = np.array([p_start[0], p_start[1], FINAL_Z], dtype=float)
    tau = min(t_land / LAND_TIME, 1.0)
    return interpolate_xyz(p_start, p_end, tau)


