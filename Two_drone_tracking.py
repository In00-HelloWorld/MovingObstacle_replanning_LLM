if __name__ == "__main__":
    args = parse_args()
    active_drone_names, trajectory_path = prepare_mission(args)

    if args.dry_run:
        print("[DRY-RUN] Trajectory prepared. Hardware connection skipped.")
        raise SystemExit(0)

    if HARDWARE_IMPORT_ERROR is not None and can_fall_back_to_planning_only(args):
        print(
            "[PLAN-ONLY] Hardware dependencies are unavailable, so only the final "
            f"planned trajectory was prepared. Import error: {HARDWARE_IMPORT_ERROR}"
        )
        raise SystemExit(0)

    _require_hardware_imports()
    cflib.crtp.init_drivers()

    clients = {}
    queues = {}
    pose_stop_event = Event()
    pose_threads = []
    qtm_client = None
    logger = MultiTrajectoryLogger(active_drone_names)
    planning_only_due_to_qtm = False

    try:
        # 1) Connect to both drones
        for name in active_drone_names:
            conf = DRONES_CONFIG[name]
            client = CrazyflieClient(conf["uri"], marker_deck_ids=conf["marker_ids"])
            if not client.ready.wait(timeout=20):
                raise TimeoutError(f"{name} connection timeout")

            # Start battery logging after connection/TOC download
            client.setup_battery_monitor(name=name, period_ms=BATTERY_LOG_PERIOD_MS)

            q = queue.Queue(maxsize=1)
            th = Thread(target=pose_sender, args=(client, q, pose_stop_event), daemon=True)
            th.start()

            clients[name] = client
            queues[name] = q
            pose_threads.append(th)

        # 2) Connect to QTM
        body_to_queue_map = {
            DRONES_CONFIG[name]["mocap_name"]: queues[name]
            for name in active_drone_names
        }
        tracked_body_names = []
        tracked_body_names.append(args.spiderpi_body_name)

        qtm_client = QualisysMultiClient(
            QTM_IP,
            body_to_queue_map,
            tracked_body_names=tracked_body_names,
        )

        print("[MAIN] Waiting for QTM stream...")
        if not qtm_client.streaming_started.wait(timeout=10):
            raise TimeoutError("QTM stream timeout")

        print("[MAIN] Warm-up extpose...")
        time.sleep(2.0)

        # 3) Reset estimators
        for name, client in clients.items():
            print(f"[MAIN] Reset estimator: {name}")
            client.reset_estimator()

        time.sleep(2.0)

        # 4) Arm
        for name, client in clients.items():
            print(f"[MAIN] Arming: {name}")
            client.cf.platform.send_arming_request(True)

        time.sleep(1.0)

        # 5) Main synchronized mission loop
        print("[MISSION] Starting synchronized two-drone mission")
        mission_start_wall = time.time()
        paused_total = 0.0
        dt_cmd = 1.0 / RATE_HZ
        spiderpi_close_prev = False
        spiderpi_replan_state = {
            "active": False,
            "done": Event(),
            "thread": None,
            "result": None,
            "error": None,
            "hold_positions": {},
        }
        spiderpi_replan_state["done"].set()

        while True:
            paused_increment = finish_spiderpi_replan_if_ready(args, spiderpi_replan_state)
            if paused_increment is not None:
                paused_total += paused_increment

            if spiderpi_replan_state["active"] and spiderpi_replan_state.get("pause_started") is not None:
                t_now = spiderpi_replan_state["pause_started"] - mission_start_wall - paused_total
            else:
                t_now = time.time() - mission_start_wall - paused_total
            if t_now > TOTAL_END_TIME:
                break

            spiderpi_close_now = False
            if not args.no_spiderpi_replan:
                (
                    spiderpi_close_now,
                    _spiderpi_position,
                    nearest_drone,
                    min_distance,
                ) = spiderpi_drone_proximity(active_drone_names, qtm_client, args)
                if (
                    spiderpi_close_now
                    and not spiderpi_close_prev
                    and not spiderpi_replan_state["active"]
                ):
                    print(
                        f"[SPIDERPI] Nearest drone: {nearest_drone}; "
                        f"distance={min_distance:.3f} m "
                        f"(xyz, threshold={args.spiderpi_replan_distance:.3f} m)"
                    )
                    start_spiderpi_replan_worker(
                        args,
                        active_drone_names,
                        qtm_client,
                        spiderpi_replan_state,
                        t_now,
                        logger,
                    )
            if qtm_client is not None:
                spiderpi_position = qtm_client.get_latest_position(args.spiderpi_body_name)
                if spiderpi_position is not None:
                    logger.add_spiderpi(t_now, spiderpi_position)
            spiderpi_close_prev = spiderpi_close_now

            for drone_name, client in clients.items():
                if spiderpi_replan_state["active"]:
                    ref_xyz = spiderpi_replan_state["hold_positions"].get(
                        drone_name,
                        desired_position_with_landing(drone_name, t_now),
                    )
                    cmd_xyz = np.array(ref_xyz, dtype=float)
                else:
                    ref_xyz = desired_position_with_landing(drone_name, t_now)
                    cmd_xyz = np.array(ref_xyz, dtype=float)
                client.send_position_setpoint(cmd_xyz, YAW_DEG)

                mocap_name = DRONES_CONFIG[drone_name]["mocap_name"]
                actual_xyz = qtm_client.get_latest_position(mocap_name)
                vbat, battery_level, pm_state = client.get_battery()

                if actual_xyz is not None:
                    logger.add(
                        drone_name=drone_name,
                        t=t_now,
                        ref_xyz=cmd_xyz,
                        actual_xyz=actual_xyz,
                        vbat=vbat,
                        battery_level=battery_level,
                        pm_state=pm_state,
                    )

            time.sleep(dt_cmd)

        print("[MISSION] Mission complete")

    except Exception as e:
        if is_qtm_or_mocap_error(e) and can_fall_back_to_planning_only(args):
            planning_only_due_to_qtm = True
            print(
                "[PLAN-ONLY] QTM/motion capture is unavailable, so the code stopped "
                "after preparing the final planned trajectory."
            )
            print(f"[PLAN-ONLY] Reason: {e}")
        else:
            print(f"[ERROR] {e}")

    finally:
        pose_stop_event.set()

        for client in clients.values():
            try:
                client.stop_battery_monitor()
            except Exception:
                pass

        for client in clients.values():
            try:
                client.cf.commander.send_stop_setpoint()
                time.sleep(0.05)
                client.cf.close_link()
            except Exception:
                pass

        if qtm_client is not None:
            try:
                qtm_client.close()
            except Exception:
                pass

        for th in pose_threads:
            th.join(timeout=1.0)

        try:
            if not planning_only_due_to_qtm:
                save_scaled_and_original_3d_figures(logger, args)
                save_tracking_error_figure(logger, ERR_FIG_NAME)
                logger.save_all_csv(
                    {
                        drone_name: CSV_LOG_FILES.get(
                            drone_name, f"{drone_name}_trajectory_log.csv"
                        )
                        for drone_name in active_drone_names
                    }
                )
                logger.save_spiderpi_csv(SPIDERPI_CSV_LOG_FILE)
        except Exception as e:
            print(f"[PLOT/CSV ERROR] {e}")

        print("[MAIN] Done.")
