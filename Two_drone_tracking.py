class BatteryMonitor:
    def __init__(self, cf, name, period_ms=200):
        self.name = name
        self.lock = Lock()
        self.vbat = None
        self.battery_level = None
        self.pm_state = None

        self.logconf = LogConfig(name=f"Battery_{name}", period_in_ms=period_ms)
        self.logconf.add_variable("pm.vbat", "float")
        self.logconf.add_variable("pm.batteryLevel", "uint8_t")
        self.logconf.add_variable("pm.state", "int8_t")

        cf.log.add_config(self.logconf)
        self.logconf.data_received_cb.add_callback(self._data_received)
        self.logconf.error_cb.add_callback(self._error)

    def start(self):
        self.logconf.start()
        print(f"[BAT] Started battery logging: {self.name}")

    def stop(self):
        try:
            self.logconf.stop()
        except Exception:
            pass

    def _data_received(self, timestamp, data, logconf):
        with self.lock:
            self.vbat = data.get("pm.vbat", None)
            self.battery_level = data.get("pm.batteryLevel", None)
            self.pm_state = data.get("pm.state", None)

    def _error(self, logconf, msg):
        print(f"[BAT] Log error on {self.name}: {msg}")

    def get_latest(self):
        with self.lock:
            return self.vbat, self.battery_level, self.pm_state


