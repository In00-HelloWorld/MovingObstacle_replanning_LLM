def current_drone_positions(active_drone_names, qtm_client):
    positions = {}
    for drone_name in active_drone_names:
        mocap_name = DRONES_CONFIG[drone_name]["mocap_name"]
        position = qtm_client.get_latest_position(mocap_name)
        if position is None:
            return None
        positions[drone_name] = position
    return positions


def spiderpi_drone_proximity(active_drone_names, qtm_client, args):
    raw_spiderpi_position = qtm_client.get_latest_position(args.spiderpi_body_name)
    if raw_spiderpi_position is None:
        return False, None, None, None

    spiderpi_position = spiderpi_effective_position(args, raw_spiderpi_position)
    min_distance = None
    nearest_drone = None
    for drone_name in active_drone_names:
        mocap_name = DRONES_CONFIG[drone_name]["mocap_name"]
        drone_position = qtm_client.get_latest_position(mocap_name)
        if drone_position is None:
            continue
        distance = float(np.linalg.norm(drone_position - spiderpi_position))
        if min_distance is None or distance < min_distance:
            min_distance = distance
            nearest_drone = drone_name

    if min_distance is None:
        return False, spiderpi_position, None, None
    return (
        min_distance <= args.spiderpi_replan_distance,
        spiderpi_position,
        nearest_drone,
        min_distance,
    )


def start_spiderpi_replan_worker(args, active_drone_names, qtm_client, state, mission_time, logger):
    raw_spiderpi_position = qtm_client.get_latest_position(args.spiderpi_body_name)
    if raw_spiderpi_position is None:
        print("[SPIDERPI] Proximity trigger fired, but SpiderPi position is unavailable.")
        return False
    spiderpi_position = spiderpi_effective_position(args, raw_spiderpi_position)

    drone_positions = current_drone_positions(active_drone_names, qtm_client)
    if drone_positions is None:
        print("[SPIDERPI] Proximity trigger fired, but one or more drone positions are unavailable.")
        return False

    state["done"].clear()
    state["result"] = None
    state["error"] = None
    state["pause_started"] = time.time()
    state["hold_positions"] = {
        drone_name: np.array(position, dtype=float)
        for drone_name, position in drone_positions.items()
    }
    state["trigger_time"] = float(mission_time)
    state["active"] = True

    def worker():
        try:
            state["result"] = run_spiderpi_triggered_replan(
                args,
                drone_positions,
                spiderpi_position,
                mission_time,
                logger,
            )
        except Exception as exc:
            state["error"] = exc
        finally:
            state["done"].set()

    thread = Thread(target=worker, daemon=True)
    state["thread"] = thread
    thread.start()
    print(
        "[SPIDERPI] SpiderPi is within the drone proximity threshold. Holding current positions while "
        "the LLM replans."
    )
    return True


def finish_spiderpi_replan_if_ready(args, state):
    global MISSION_SCHEDULES, MISSION_END_TIME, TOTAL_END_TIME

    if not state.get("active") or not state["done"].is_set():
        return None

    state["active"] = False
    if state.get("thread") is not None:
        state["thread"].join(timeout=1.0)

    if state.get("error") is not None:
        print(f"[SPIDERPI] Live replan failed: {state['error']}")
        return None

    output_path, schedules, payload = state["result"]
    pause_started = state.get("pause_started")
    if pause_started is None:
        pause_started = time.time()
    pause_duration = max(0.0, time.time() - pause_started)
    trigger_time = float(state.get("trigger_time", 0.0))
    live_schedules = {
        agent_id: [
            (float(t) + trigger_time, np.array(xyz, dtype=float))
            for t, xyz in schedule
        ]
        for agent_id, schedule in schedules.items()
    }
    problem_id = payload.get("problem_id", args.problem_id)
    configure_problem_context(problem_id, live_schedules)
    MISSION_SCHEDULES = live_schedules
    MISSION_END_TIME = max(schedule[-1][0] for schedule in live_schedules.values())
    TOTAL_END_TIME = MISSION_END_TIME + LAND_TIME

    verification = payload.get("verification", {})
    print(f"[SPIDERPI] Live replan accepted: {output_path}")
    print("[SPIDERPI] Replanned mission is now active.")
    print(f"[SPIDERPI] Pass: {verification.get('pass')}")
    print(f"[SPIDERPI] Reason: {verification.get('reason', '')}")
    print(f"[SPIDERPI] New mission end: {MISSION_END_TIME:.2f}s")
    print(f"[SPIDERPI] Paused mission clock for {pause_duration:.2f}s while replanning.")
    state["pause_started"] = None
    return pause_duration


