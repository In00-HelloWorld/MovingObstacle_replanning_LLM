class MultiTrajectoryLogger:
    def __init__(self, drone_names):
        self.lock = Lock()
        self.samples = {name: [] for name in drone_names}
        self.spiderpi_samples = []

    def add(self, drone_name, t, ref_xyz, actual_xyz, vbat=None, battery_level=None, pm_state=None):
        with self.lock:
            self.samples[drone_name].append({
                "t": float(t),
                "ref": np.array(ref_xyz, dtype=float),
                "act": np.array(actual_xyz, dtype=float),
                "err": float(np.linalg.norm(np.array(actual_xyz) - np.array(ref_xyz))),
                "vbat": None if vbat is None else float(vbat),
                "battery_level": None if battery_level is None else float(battery_level),
                "pm_state": None if pm_state is None else int(pm_state),
            })

    def add_spiderpi(self, t, xyz):
        with self.lock:
            self.spiderpi_samples.append({
                "t": float(t),
                "pos": np.array(xyz, dtype=float),
            })

    def get_arrays(self, drone_name):
        with self.lock:
            data = self.samples[drone_name]
            if len(data) == 0:
                return (
                    np.zeros((0,)),
                    np.zeros((0, 3)),
                    np.zeros((0, 3)),
                    np.zeros((0,))
                )
            t = np.array([d["t"] for d in data], dtype=float)
            ref = np.vstack([d["ref"] for d in data])
            act = np.vstack([d["act"] for d in data])
            err = np.array([d["err"] for d in data], dtype=float)
            return t, ref, act, err

    def get_spiderpi_arrays(self):
        with self.lock:
            data = self.spiderpi_samples
            if len(data) == 0:
                return np.zeros((0,)), np.zeros((0, 3))
            t = np.array([d["t"] for d in data], dtype=float)
            pos = np.vstack([d["pos"] for d in data])
            return t, pos

    def save_csv(self, drone_name, filename):
        with self.lock:
            data = self.samples[drone_name]

            with open(filename, "w", newline="") as f:
                writer = csv.writer(f)
                writer.writerow([
                    "time",
                    "ref_x", "ref_y", "ref_z",
                    "act_x", "act_y", "act_z",
                    "err",
                    "vbat",
                    "battery_level",
                    "pm_state",
                ])
                for d in data:
                    writer.writerow([
                        d["t"],
                        d["ref"][0], d["ref"][1], d["ref"][2],
                        d["act"][0], d["act"][1], d["act"][2],
                        d["err"],
                        d["vbat"],
                        d["battery_level"],
                        d["pm_state"],
                    ])

        print(f"[CSV] Saved log for {drone_name}: {filename}")

    def save_spiderpi_csv(self, filename):
        with self.lock:
            data = self.spiderpi_samples

            with open(filename, "w", newline="") as f:
                writer = csv.writer(f)
                writer.writerow([
                    "time",
                    "x", "y", "z",
                ])
                for d in data:
                    writer.writerow([
                        d["t"],
                        d["pos"][0], d["pos"][1], d["pos"][2],
                    ])

        print(f"[CSV] Saved log for SpiderPi: {filename}")

    def save_all_csv(self, filename_map):
        for drone_name, filename in filename_map.items():
            self.save_csv(drone_name, filename)


