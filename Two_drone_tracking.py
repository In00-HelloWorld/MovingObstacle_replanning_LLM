def scaled_to_original(args, point):
    scale = float(args.coordinate_scale)
    if abs(scale) <= 1e-12:
        return np.array(point, dtype=float)
    offset = np.array(args.coordinate_offset, dtype=float)
    return (np.array(point, dtype=float) - offset) / scale


