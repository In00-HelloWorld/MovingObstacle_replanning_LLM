def _as_schedule_waypoint(raw_waypoint, agent_id, index, source_path):
    try:
        t = float(raw_waypoint["t"])
        xyz = np.array(
            [
                float(raw_waypoint["x"]),
                float(raw_waypoint["y"]),
                float(raw_waypoint["z"]),
            ],
            dtype=float,
        )
    except (KeyError, TypeError, ValueError) as exc:
        raise ValueError(
            f"Invalid waypoint {index} for {agent_id} in {source_path}: {raw_waypoint}"
        ) from exc
    if not np.isfinite(t) or not np.all(np.isfinite(xyz)):
        raise ValueError(
            f"Non-finite waypoint {index} for {agent_id} in {source_path}: {raw_waypoint}"
        )
    return t, xyz


def load_schedules_from_trajectory_json(
    trajectory_path,
    require_verified=True,
):
    path = Path(trajectory_path)
    payload = json.loads(path.read_text(encoding="utf-8"))

    verification = payload.get("verification", {})
    if require_verified and verification.get("pass") is not True:
        reason = verification.get("reason", "verification.pass is not true")
        raise RuntimeError(
            f"Refusing to track unverified trajectory {path}: {reason}. "
            "Use --allow-unverified if this is intentional."
        )

    schedules = {}
    for trajectory in payload.get("agent_trajectories", []):
        agent_id = trajectory.get("agent_id")
        if agent_id not in DRONES_CONFIG:
            raise ValueError(
                f"Trajectory for {agent_id!r} has no matching DRONES_CONFIG entry."
            )
        waypoints = trajectory.get("waypoints", [])
        if not waypoints:
            raise ValueError(f"Trajectory for {agent_id!r} has no waypoints.")

        schedule = [
            _as_schedule_waypoint(raw_waypoint, agent_id, index, path)
            for index, raw_waypoint in enumerate(waypoints)
        ]
        schedule.sort(key=lambda item: item[0])
        for (prev_t, _), (next_t, _) in zip(schedule, schedule[1:]):
            if next_t <= prev_t:
                raise ValueError(
                    f"Trajectory times for {agent_id!r} must be strictly increasing."
                )
        schedules[agent_id] = schedule

    if not schedules:
        raise ValueError(f"No agent_trajectories found in {path}.")
    return schedules, payload


def _problem_workspace_bounds(problem, schedules):
    points = []
    for schedule in schedules.values():
        points.extend([xyz for _, xyz in schedule])
    for obstacle in problem.obstacles:
        points.append(np.array(obstacle.min_corner, dtype=float))
        points.append(np.array(obstacle.max_corner, dtype=float))
    if not points:
        return WORKSPACE_X, WORKSPACE_Y, WORKSPACE_Z

    stacked = np.vstack(points)
    mins = np.min(stacked, axis=0)
    maxs = np.max(stacked, axis=0)
    margin = np.array([0.35, 0.35, 0.20])
    return (
        (float(mins[0] - margin[0]), float(maxs[0] + margin[0])),
        (float(mins[1] - margin[1]), float(maxs[1] + margin[1])),
        (0.0, float(max(1.5, maxs[2] + margin[2]))),
    )


def _mission_points_from_problem(problem, active_agent_ids):
    points = {}
    for agent in problem.agents:
        if agent.agent_id not in active_agent_ids:
            continue
        agent_points = {}
        start = problem.charging_stations[agent.start_station_index]
        agent_points[f"S{agent.start_station_index}"] = np.array(start, dtype=float)
        for target_index in agent.required_target_indices:
            agent_points[f"T{target_index}"] = np.array(
                problem.targets[target_index], dtype=float
            )
        agent_points["G"] = np.array(agent.final_goal, dtype=float)
        points[agent.agent_id] = agent_points
    return points


