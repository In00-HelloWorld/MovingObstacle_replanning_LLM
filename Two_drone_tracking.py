    if args.trajectory_source == "built-in":
        active_names = list(MISSION_SCHEDULES.keys())
        print("[PLAN] Using legacy built-in tracking schedule.")
        return active_names, None

    if args.trajectory_source == "llm":
        trajectory_path, payload = _generate_llm_trajectory(args)
    elif args.trajectory_source == "reference":
        trajectory_path, payload = _generate_reference_trajectory(args)
    else:
        trajectory_path = Path(args.trajectory_json)
        payload = None

    schedules, loaded_payload = load_schedules_from_trajectory_json(
        trajectory_path,
        require_verified=not args.allow_unverified,
    )
    payload = loaded_payload if payload is None else payload
    problem_id = payload.get("problem_id", args.problem_id)
    configure_problem_context(problem_id, schedules)

    MISSION_SCHEDULES = schedules
    MISSION_END_TIME = max(schedule[-1][0] for schedule in schedules.values())
    TOTAL_END_TIME = MISSION_END_TIME + LAND_TIME

    verification = payload.get("verification", {})
    print(f"[PLAN] Loaded verified trajectory: {trajectory_path}")
    print(f"[PLAN] Problem: {problem_id}")
    print(f"[PLAN] Pass: {verification.get('pass')}")
    print(f"[PLAN] Reason: {verification.get('reason', '')}")
