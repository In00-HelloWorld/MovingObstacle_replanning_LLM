def _generate_llm_trajectory(args):
    from tool_pipeline import run_llm_refinement

    output_path = Path(args.trajectory_output)
    if args.reuse_existing_llm_trajectory and output_path.exists():
        try:
            payload = json.loads(output_path.read_text(encoding="utf-8"))
            if payload.get("problem_id", args.problem_id) != args.problem_id:
                raise ValueError(
                    f"problem_id={payload.get('problem_id')!r} does not match requested {args.problem_id!r}"
                )
            load_schedules_from_trajectory_json(
                output_path,
                require_verified=not args.allow_unverified,
            )
            print(f"[PLAN] Reusing existing trajectory JSON: {output_path}")
            return output_path, payload
        except Exception as exc:
            print(f"[PLAN] Cannot reuse existing trajectory JSON ({exc}); regenerating.")

    print(f"[PLAN] Running LLM tool pipeline for {args.problem_id}...")
    payload = run_llm_refinement(
        output_path=args.trajectory_output,
        problem_id=args.problem_id,
        max_refinement_turns=args.max_refinement_turns,
        use_nmpc_cbf=not args.no_nmpc_cbf,
        use_retiming=not args.no_retiming,
        target_time=args.target_time,
        nmpc_backend=args.nmpc_backend,
        full_apply_steps=args.full_apply_steps,
        full_max_duration=args.full_max_duration,
        estimate_required_time_on_failure=True,
        plot=not args.no_planner_plot,
    )
    return output_path, payload


