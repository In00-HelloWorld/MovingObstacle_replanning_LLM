class QualisysMultiClient(Thread):
    def __init__(self, ip_address, body_to_queue_map, tracked_body_names=None):
        super().__init__(daemon=True)
        self.ip_address = ip_address
        self.body_to_queue_map = body_to_queue_map
        self.tracked_body_names = set(tracked_body_names or [])
        self.all_body_names = set(body_to_queue_map.keys()) | self.tracked_body_names
        self.stop_event = Event()
        self.streaming_started = Event()
        self.transforms = {name: MocapTransform() for name in body_to_queue_map.keys()}

        self.latest_pose_lock = Lock()
        self.latest_positions = {name: None for name in self.all_body_names}

        self.start()

    def get_latest_position(self, body_name):
        with self.latest_pose_lock:
            p = self.latest_positions.get(body_name, None)
            if p is None:
                return None
            return np.array(p, dtype=float)

    def run(self):
        asyncio.run(self._life_cycle())

    async def _life_cycle(self):
        connection = await qtm.connect(self.ip_address, version="1.24")
        if connection is None:
            raise RuntimeError("Failed to connect to QTM")

        params = await connection.get_parameters(parameters=["6d"])
        xml = ET.fromstring(params)
        labels = [label.text.strip() for label in xml.findall("*/Body/Name")]
        print(f"[QTM] Bodies: {labels}")

        def on_packet(packet):
            header, bodies = packet.get_6d()
            if bodies is None:
                return

            for body_name in self.all_body_names:
                if body_name not in labels:
                    continue

                idx = labels.index(body_name)
                pos, ori = bodies[idx]

                if np.any(np.isnan(pos)):
                    continue

                raw_position = np.array(pos) / 1e3
                if body_name in self.body_to_queue_map:
                    R = Rotation.from_matrix(np.reshape(ori.matrix, (3, 3), order="F"))
                    yaw, pitch, roll = R.as_euler("ZYX", degrees=False)
                    p_cf, q_cf = self.transforms[body_name].update_and_transform(
                        raw_position,
                        [yaw, pitch, roll]
                    )
                else:
                    p_cf = raw_position
                    q_cf = None

                if np.all(np.isfinite(p_cf)):
                    if body_name in self.body_to_queue_map:
                        self.streaming_started.set()

                    with self.latest_pose_lock:
                        self.latest_positions[body_name] = p_cf.copy()

                    if body_name in self.body_to_queue_map:
                        q = self.body_to_queue_map[body_name]
                        try:
                            if q.full():
                                q.get_nowait()
                            q.put_nowait((*p_cf, *q_cf))
                        except Exception:
                            pass

        await connection.stream_frames(components=["6d"], on_packet=on_packet)

        while not self.stop_event.is_set():
            await asyncio.sleep(0.1)

        await connection.stream_frames_stop()
        connection.disconnect()

    def close(self):
        self.stop_event.set()
        self.join(timeout=1.0)
        if self.is_alive():
            print("[QTM] Close timed out; letting daemon thread exit with the process.")


